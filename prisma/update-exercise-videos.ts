async function main() {
  let updated = 0;
  for (const { id, youtubeUrl } of EXERCISE_VIDEOS) {
    try {
      await prisma.exercise.update({
        where: { id },
        data: { mediaType: "YOUTUBE", mediaUrl: youtubeUrl },
      });
      updated++;
    } catch (err) {
      console.error(`Falha ao atualizar ${id}:`, err);
    }
  }
  console.log(`${updated}/${EXERCISE_VIDEOS.length} exercício(s) atualizado(s) com vídeo do YouTube.`);
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
